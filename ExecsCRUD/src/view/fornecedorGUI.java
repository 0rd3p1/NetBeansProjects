    private void btnBuscarIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarIdActionPerformed
        int id = Integer.valueOf(txtId.getText());
        Optional<fornecedor> f = fornecedorController.getById(id);
        f.ifPresentOrElse(fs -> {
            txtNf.setText(fs.getRs());
            txtRs.setText(fs.getNf());
            txtCnpj.setText(String.valueOf(fs.getCnpj()));
        }, () -> {
            JOptionPane.showMessageDialog(this, "ID nao encontrado!");
            txtId.setText("");
            txtId.requestFocus();
        });
    }//GEN-LAST:event_btnBuscarIdActionPerformed

    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        txtId.setText("");
        txtNf.setText("");
        txtRs.setText("");
        txtCnpj.setText("");
        txtId.requestFocus();
        popularTabela(fornecedorController.getAll());
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String rS = txtRs.getText();
        String nF = txtNf.getText();
        int cnpj = Integer.valueOf(txtCnpj.getText());

        fornecedorController.add(rS, nF, cnpj);

        JOptionPane.showMessageDialog(this, "Fornecedor Salvo!");
        popularTabela(fornecedorController.getAll());
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int res = JOptionPane.showConfirmDialog(
                this,
                "Tem certeza?",
                "Confirmar exclusao",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
        );
        if (res == JOptionPane.YES_OPTION) {
            int id = Integer.valueOf(txtId.getText());
            if (fornecedorController.delete(id)) {
                JOptionPane.showMessageDialog(this, "Fornecedor excluido!");
            } else {
                JOptionPane.showMessageDialog(this, "Fornecedor nao encontrado!");
            }
            popularTabela(fornecedorController.getAll());

        } else {
            JOptionPane.showMessageDialog(this, "Abre o olho japones!");
        }
        popularTabela(fornecedorController.getAll());
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int id = Integer.valueOf(txtId.getText());
        String rS = txtNf.getText();
        String nF = txtRs.getText();
        int cnpj = Integer.valueOf(txtCnpj.getText());

        if (fornecedorController.update(id, rS, nF, cnpj)) {
            JOptionPane.showMessageDialog(this, "Fornecedor alterado!");
        } else {
            JOptionPane.showMessageDialog(this, "Fornecedor nao encontrado!");
        }
        popularTabela(fornecedorController.getAll());
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void tblFornecedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFornecedoresMouseClicked
        int id = (int) tblFornecedores.getValueAt(tblFornecedores.getSelectedRow(), 0);
        Optional<fornecedor> f = fornecedorController.getById(id);
        f.ifPresentOrElse(fs -> {
            txtId.setText(String.valueOf(fs.getId()));
            txtNf.setText(fs.getRs());
            txtRs.setText(fs.getNf());
            txtCnpj.setText(String.valueOf(fs.getCnpj()));
        }, () -> {
            JOptionPane.showMessageDialog(this, "ID nao encontrado!");
            txtId.setText("");
            txtId.requestFocus();
        });
    }//GEN-LAST:event_tblFornecedoresMouseClicked

