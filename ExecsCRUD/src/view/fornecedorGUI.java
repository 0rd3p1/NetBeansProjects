    public void popularTabela(ArrayList<fornecedor> fornecedores) {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Razao Social");
        model.addColumn("Nome Fantasia");
        model.addColumn("CNPJ");

        for (fornecedor u : fornecedores) {
            model.addRow(new Object[]{u.getId(), u.getRs(), u.getNf(), u.getCnpj()});
        }
        tblFornecedores.setModel(model);
    }

